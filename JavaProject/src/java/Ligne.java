public class Ligne {
    private int num;
    private int matricule;

    public Ligne(int num, int matricule) {
        this.num = num;
        this.matricule = matricule;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getMatricule() {
        return matricule;
    }

    public void setMatricule(int matricule) {
        this.matricule = matricule;
    }
    
    
}
